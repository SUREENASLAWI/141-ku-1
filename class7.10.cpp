//Print the coordinates in Quadrant 1 from (0, 0) to (2, 2)

#include <iostream>
using namespace std;

int main(){
	
	for(int i=0; i<=2; i++){
		for(int j=0; j<=2; j++){
			cout << "( " << i << " , " << j << " )" << endl;
		}
	}
	
	return 0;
}