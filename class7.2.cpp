//Write a while loop and for loop program that prints the values from 0 to 20, in increments of 5
//Example output > 0 5 10 15 20

#include <iostream>
using namespace std;

int main(){
	
	//c = counter
	for(int c=0; c<=20; c+=5){
		cout << c << " " ;
	}
	
	return 0;
}
