#include <iostream>
using namespace std;

int main(){
	
	unsigned int c=5;
	
	cout << "c befor postincrement: " << c << endl;
	cout << "  posincrementing c: " << c++ << endl;
	cout << "c after postincrement: " << c << endl;
	
	cout << endl;
	
	c=5;
	cout << "c befor postincrement: " << c << endl;
	cout << "  posincrementing c: " << ++c << endl;
	cout << "c after postincrement: " << c << endl;
	
	
	
	return 0;
}