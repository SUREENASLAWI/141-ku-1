//Write a while loop and for loop program that prints the values from 20 to 0, in decrements of 5
//Example output > 20 15 10 5 0

#include <iostream>
using namespace std;

int main(){
	
	//c = counter
	for(int c=20; c>=0; c-=5){
		cout << c << " ";
	}
	
	return 0;
}