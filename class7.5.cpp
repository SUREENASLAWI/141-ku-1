//Write a while loop and for loop program that prints the values from 99 to 0, in decrements of 11
//Example output > 99 88 77 66 55 44 33 22 11 0

#include <iostream>
using namespace std;

int main(){
	
	for(int i=99; i>-0; i-=11){
		cout << i << " ";
	}
	
	return 0;
}